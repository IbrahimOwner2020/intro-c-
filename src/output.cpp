#include <iostream>
using namespace std;

int main()
{
    // use '\n' to print a new line
    cout << "Hello World! \n \n";
    // use 'endl' to print multiple lines
    cout << "I am a C++ program" << endl;

    // use '\t' to print a tab
    cout << "I am learning \t C++";

    // use '\\' to print a backslash
    cout << "I am learning \\ C++";

    // use '\"' to print a double quote
    cout << "I am learning \" C++";
    return 0;
}
